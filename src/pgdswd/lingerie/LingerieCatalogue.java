    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //images/lingerie_00 + list[count] + .png
        ImageIcon imgIcon = createImageIcon("images/" + imgFilesPath[count]);
        //html/00 + list[count] + .html
        URL htlmUrl = createFileUrl("html/" + htmlFilesPath[count]);
        if(imgIcon != null){
          try{
            jLabel1.setIcon(imgIcon);
            if(htlmUrl != null){
              jEditorPane1.setPage(htlmUrl);
            }else{
              jEditorPane1.setPage(createFileUrl(" "));
            }
          }catch(IOException ex){
            javax.swing.JOptionPane.showMessageDialog(null, "File not found", "Error", 0);
          }
          
          if(count == 9){
             jButton2.setText("First Catalogue Item");
            count = 0;
          }else{
              jButton2.setText("Next Catalogue Item");
              ++count;}
        }else{
            javax.swing.JOptionPane.showMessageDialog(null, "No image found", "Error", 0);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

