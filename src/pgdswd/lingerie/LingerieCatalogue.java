    /**
     * A function createImageIcon of the lingerie
     * @param path a string path to the lingerie icon
     * @return the lingerie icon
     */
    private ImageIcon createImageIcon(String path)
    {
        URL imgPath = getClass().getResource(path);
        if(imgPath != null){
            return new ImageIcon(imgPath);
        }
        doMsg(path);
        return null;
    }  

    /**
     * A function createFileUrl of the html file with details of the lingerie
     * @param path a string path to the html file
     * @return the lingerie html file
     */
    private URL createFileUrl(String path){
        URL url = getClass().getResource(path);
        if(url == null){
            doMsg(path);
        }
        return url;
    }
  
    private void doMsg(String path){
        javax.swing.JOptionPane.showMessageDialog(null, "The path to " + path + " is incorect", "Error!", 0);
    }
    
    
