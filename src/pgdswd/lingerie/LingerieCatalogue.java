    private ImageIcon createImageIcon(String path)
    {
        URL imgPath = getClass().getResource(path);
        if(imgPath != null){
            return new ImageIcon(imgPath);
    }
        doMsg(path);
        return null;
    }  

    private URL createFileUrl(String path){
        URL url = getClass().getResource(path);
        if(url == null){
            doMsg(path);
        }
        return url;
    }
  
    private void doMsg(String path){
        javax.swing.JOptionPane.showMessageDialog(null, "The path to " + path + " is incorect", "Error!", 0);
    }
    
    
